    private void tfAbastQtdLitrosKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tfAbastQtdLitrosKeyReleased
        // TODO add your handling code here:
        nota.setQtdLitros(Double.parseDouble(tfAbastQtdLitros.getText()));
        
        if (rbAbastDiesel.isSelected()) {
            nota.setCombustivel(EnumPrecoCombustivel.OLEO_DIESEL);
            nota.setTotalCombustivel(EnumPrecoCombustivel.OLEO_DIESEL.getValor()*nota.getQtdLitros());
        }
        if (rbAbastComum.isSelected()){
            nota.setCombustivel(EnumPrecoCombustivel.GAS_COMUM);
            nota.setTotalCombustivel(EnumPrecoCombustivel.GAS_COMUM.getValor()*nota.getQtdLitros());
        }
        if (rbAbastAditivada.isSelected()){
            nota.setCombustivel(EnumPrecoCombustivel.GAS_ADITIVADA);
            nota.setTotalCombustivel(EnumPrecoCombustivel.GAS_ADITIVADA.getValor()*nota.getQtdLitros());
        }
        if (rbAbastAlcool.isSelected()){
            nota.setCombustivel(EnumPrecoCombustivel.ALCOOL);
            nota.setTotalCombustivel(EnumPrecoCombustivel.ALCOOL.getValor()*nota.getQtdLitros());
        }
        this.ftAbastTotalCombustivel.setText("R$ " + df.format(nota.getTotalCombustivel()));
        //String aux = ftAbastTotalCombustivel.getText().replace("R$ ", "").replace(",",".");
        //System.out.println("Aux " + aux);
        //double v = Double.parseDouble(aux);
    }//GEN-LAST:event_tfAbastQtdLitrosKeyReleased

