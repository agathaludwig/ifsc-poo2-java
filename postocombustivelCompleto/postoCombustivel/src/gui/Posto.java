    private void btCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btCalcularActionPerformed
        // TODO add your handling code here:
        if (rbPagamentoAvista.isSelected()) {
            nota.setAvista(true);
            double desconto = (nota.getTotalCombustivel())*0.1;
            nota.setTotal(nota.getTotalOleo() + nota.getTotalCombustivel() - desconto);
            this.tfPagamentoTotal.setText("R$ " + df.format(nota.getTotal()));        
        }
        else if (rbPagamentoPrazo.isSelected()) {
            nota.setDias(Integer.parseInt(this.tfPagamentoDias.getText()));
            if (nota.getDias() > 30) {
                double acrescimo = (nota.getTotalOleo()+nota.getTotalCombustivel())*0.1;
                nota.setTotal(nota.getTotalOleo() + nota.getTotalCombustivel() + acrescimo);
                this.tfPagamentoTotal.setText("R$ " + df.format(nota.getTotal()));        
            }
            else {
                nota.setTotal(nota.getTotalOleo() + nota.getTotalCombustivel());
                this.tfPagamentoTotal.setText("R$ " + df.format(nota.getTotal()));
            }
        }
        
        if (rbAbastDiesel.isSelected()) {
            try {
                Controle.resDiesel.saida(nota.getQtdLitros());
            } catch (ExcecaoReservatorio er) {
                new DialogError(this, true, er.getMessage()).setVisible(true);
              }
        }
        if (rbAbastComum.isSelected()){
             try {
                Controle.resComum.saida(nota.getQtdLitros());
            } catch (ExcecaoReservatorio er) {
                new DialogError(this, true, er.getMessage()).setVisible(true);
              }
        }
        if (rbAbastAditivada.isSelected()){
             try {
                Controle.resAditivada.saida(nota.getQtdLitros());
            } catch (ExcecaoReservatorio er) {
                new DialogError(this, true, er.getMessage()).setVisible(true);
              }
        }
        if (rbAbastAlcool.isSelected()){
            nota.setCombustivel(EnumPrecoCombustivel.ALCOOL);
            nota.setTotalCombustivel(EnumPrecoCombustivel.ALCOOL.getValor()*nota.getQtdLitros());
             try {
                Controle.resAlcool.saida(nota.getQtdLitros());
            } catch (ExcecaoReservatorio er) {
                new DialogError(this, true, er.getMessage()).setVisible(true);
              }
        }
    }//GEN-LAST:event_btCalcularActionPerformed

